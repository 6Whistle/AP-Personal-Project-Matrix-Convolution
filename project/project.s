;This Function do Matirx padding, convolution, and sub-Sampling at once
Matrix_function
		MOV r2, #0			;Matrix's row
		MOV r3, #0			;Matrix's col

;@ means Current point	
;		@A	B	C				a	a		b
;		D		E  F		*		a	@a	b
;		G 		H  I				c	c		d
First_Line_First_Convolution

;(A + B + D + E) * a
		LDMFD r1, {r9-r10}		;A and B Load
		BL FloatingAdd			;A + B
		MOV r10, r8
		LDR r9, [r1, #12]			;C Load
		BL FloatingAdd			;A + B + C
		MOV r10, r8
		LDR r9, [r1, #16]			;D Load
		BL FloatingAdd			;A + B + C + D
		MOV r10,  r8
		LDR r9, [r0, #0]			;a Load
		BL FloatingMul				;SUM = (A + B + C + D) * a	
		MOV r4, r8


;(C + F) * b
		LDR r9, [r1, #8]			;C Load
		LDR r10, [r1, #20]		;F Load
		BL FloatingAdd			;C + F
		MOV r10, r8
		LDR r9, [r0, #4]			;b Load
		BL FloatingMul				;(C + F) * b
		MOV r9, r8
		MOV r10, r4
		BL FloatingAdd			;SUM += (C + F) * b
		MOV r4, r8

		
;(G + H) * c	
		LDR r9, [r1, #24]			;G Load
		LDR r10, [r1, #28]		;H Load
		BL FloatingAdd			;G + H
		MOV r10, r8
		LDR r9, [r0, #0x00000100]	;c Load
		BL FloatingMul				;(G + H) * c
		MOV r9, r8
		MOV r10, r4
		BL FloatingAdd			;SUM += (G + H) * c
		MOV r4, r8


;I * d
		LDR r10, [r1, #32]		;I Load
		LDR r9, [r0, #0x00000104]	;d Load
		BL FloatingMul				;I * d
		MOV r9, r8
		MOV r10, r4
		BL FloatingAdd			;SUM += I * d
		
		STR r8, [sp], #4			;Save SUM
		ADD r0, #8					;Move point to next next(Sub_Sampling)
		ADD r3, #2					;Col += 2


;		@A	B	C				a	b		c
;		D  	E  F		*		a	@b	c
;		G  	H  I				d	e		f
First_Line_Middle_Convolution
		
;(A + D) * a
		LDR r9, [r1, #0]		;Load A
		LDR r10, [r1, #12]	;Load D
		BL FloatingAdd		;A + D
		MOV r10, r8
		LDR r9, [r0, #-4]		;Load a
		BL FloatingMul			;(A + D) * a
		MOV r4, r8				;SUM = (A + D) * a
		
;(B + E) * b
		LDR r9, [r1, #4]		;Load B
		LDR r10, [r1, #16]	;Load E
		BL FloatingAdd		;(B + E)
		MOV r10, r8
		LDR r9, [r0, #0]		;Load b
		BL FloatingMul			;(B + E) * b
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += (B + E) * b
		MOV r4, r8
		
;(C + F) * c
		LDR r9, [r1, #8]		;Load C
		LDR r10, [r1, #20]	;Load F
		BL FloatingAdd		;C + F
		MOV r10, r8
		LDR r9, [r0, #4]		;Load c
		BL FloatingMul			;(C + F) * c
		MOV r10, r8
		MOV r9, r4				
		BL FloatingAdd		;SUM += (C + F) * c
		MOV r4, r8
	
;G * d
		LDR r10, [r1, #24]	;Load G
		LDR r9, [r0, #0x000000FC]	;Load d
		BL FloatingMul			;G * d
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += G * d
		MOV r4, r8
		
;H * e
		LDR r10, [r1, #28]	;Load H
		LDR r9, [r0, #0x00000100]	;Load e
		BL FloatingMul			;H * e
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += H * e
		MOV r4, r8
		
;I * f
		LDR r10, [r1, #32]	;Load I
		LDR r9, [r0, #0x00000104]	;Load f
		BL FloatingMul			;I * f
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += I * f
		
		STR r8, [sp], #4		;Save SUM
		
		CMP r3, #62			;if col is little than 62
		ADDLT r3, #2			;col++
		ADDLT r0, #8			;Move point to next next(Sub Sampling)
		BLT First_Line_Middle_Convolution	;if col is 62, Move to next next row
		
		MOV r3, #0				;col Clear
		ADD r2, #2				;row += 2
		ADD r0, #0x00000108		;Move point to point[row + 2][0]


;		@A	B	C				a	a		b
;		D  	E  F		*		c	@c	d
;		G  	H  I				e	e		f
Middle_Line_First_Convolution
		
;(A + B) * a
		LDMFD r1, {r9-r10}		;Load A and B
		BL FloatingAdd			;A + B
		MOV r10,  r8
		LDR r9, [r0, #-0x00000100]	;Load a
		BL FloatingMul				;SUM = (A + B) * a
		MOV r4, r8
		
;(D + E) * c
		LDR r9, [r1, #12]			;Load D
		LDR r10, [r1, #16]		;Load E
		BL FloatingAdd			;D + F
		MOV r10, r8
		LDR r9, [r0, #0]			;Load c
		BL FloatingMul				;(D + E) * c
		MOV r9, r8					
		MOV r10, r4
		BL FloatingAdd			;SUM += (D + E) * c
		MOV r4, r8

;(G + H) * e
		LDR r9, [r1, #24]			;Load G
		LDR r10, [r1, #28]		;Load H
		BL FloatingAdd			;G + H
		MOV r10, r8
		LDR r9, [r0, #0x00000100]	;Load e
		BL FloatingMul				;(G + H) * e
		MOV r9, r8
		MOV r10, r4
		BL FloatingAdd			;SUM += (G + H) * e
		MOV r4, r8

;C * b		
		LDR r10, [r1, #8]			;Load C			
		LDR r9, [r0, #-0x000000FC]		;Load b
		BL FloatingMul				;C * b
		MOV r9, r8
		MOV r10, r4
		BL FloatingAdd			;SUM += C * b
		MOV r4, r8
		
;F * d
		LDR r10, [r1, #20]		;Load F
		LDR r9, [r0, #0x00000004]		;Load d
		BL FloatingMul				;F * d
		MOV r9, r8
		MOV r10, r4
		BL FloatingAdd			;SUM += F * d
		MOV r4, r8
		
;I * f
		LDR r10, [r1, #32]		;Load I
		LDR r9, [r0, #0x00000104]		;Load f
		BL FloatingMul				;I * f
		MOV r9, r8
		MOV r10, r4
		BL FloatingAdd			;SUM += I * f
		MOV r4, r8
		
		STR r8, [sp], #4			;Save SUM
		ADD r0, #8					;Move point to next next(Sub-Sampling)
		ADD r3, #2					;col += 2
		
		
;		@A	B	C				a	b		c
;		D  	E  F		*		d	@e	f
;		G  	H  I				g	h		i
Middle_Line_Middle_Convolution
;A * a
		LDR r10, [r1, #0]		;Load A
		LDR r9, [r0, #-0x00000104]		;Load a
		BL FloatingMul			;SUM = A * a
		MOV r4, r8

;B * b
		LDR r10, [r1, #4]		;Load B
		LDR r9, [r0, #-0x00000100]		;Load b
		BL FloatingMul			;B * b
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += B * b
		MOV r4, r8

;C * c
		LDR r10, [r1, #8]		;Load C
		LDR r9, [r0, #-0x000000FC]		;Load c
		BL FloatingMul			;C * c
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += C * c
		MOV r4, r8

;D * d
		LDR r10, [r1, #12]	;Load D
		LDR r9, [r0, #-0x00000004]		;Load d
		BL FloatingMul			;D * d
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += D * d
		MOV r4, r8

;E * e
		LDR r10, [r1, #16]	;Load E
		LDR r9, [r0, #0]		;Load e
		BL FloatingMul			;E * e
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += E * e
		MOV r4, r8		
		
;F * f
		LDR r10, [r1, #20]	;Load F
		LDR r9, [r0, #0x00000004]	;Load f
		BL FloatingMul			;F * f
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += F * f
		MOV r4, r8
	
;G * g
		LDR r10, [r1, #24]	;Load G
		LDR r9, [r0, #0x000000FC]	;Load g
		BL FloatingMul		;G * g
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += G * g
		MOV r4, r8
		
;H * h
		LDR r10, [r1, #28]	;Load H
		LDR r9, [r0, #0x00000100]	;Load h
		BL FloatingMul			;H * h
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += H * h
		MOV r4, r8
		
;I * i
		LDR r10, [r1, #32]	;Load I
		LDR r9, [r0, #0x00000104]	;Load i
		BL FloatingMul			;I * i
		MOV r10, r8
		MOV r9, r4
		BL FloatingAdd		;SUM += I * i
		
		STR r8, [sp], #4		;Save SUM
		
		CMP r3, #62			;if col is little than 62
		ADDLT r3, #2			;col += 2
		ADDLT r0, #8			;Move point to next next(Sub-Sampling)
		BLT Middle_Line_Middle_Convolution		;repeat cell Convolution
		
		CMP r2, #62			;if col is 62 and row is little than 62
		ADDLT r2, #2			;row += 2
		MOVLT r3, #0			;col = 0
		ADDLT r0, #0x00000108	;Move point[row + 2][0]
		BLT Middle_Line_First_Convolution		;repeat line Convolution

		;if col and row is 62
		MOV pc, #0			;Program end


		
;multiply two positive intenger(float form) and float
;r6 ~ r12 register use
FloatingMul					;r9 is must be integer, r10 is float. result will store at r8 
		MOV r11, r9, LSL #1		;delete sign
		MOV r12, r10, LSL #1	;delete sign
		
		CMP r11, #0				;check 0
		MOVEQ r8, r11
		MOVEQ pc, lr
		
		CMP r12, #0				;check 0
		MOVEQ r8, r12
		MOVEQ pc, lr
		
		MOV r11, r11, LSR #24		;r11 and r12 is exponent
		MOV r12, r12, LSR #24
		
		MOV r6, r9, LSL #9			;r6 and r7 is mantissa
		MOV r7, r10, LSL #9
		MOV r6, r6, LSR #25
		MOV r7, r7, LSR #9

		ADD r6, #0x00000080			;1.mantissa(8bit size because it is intenger)
		ADD r7, #0x00800000 			;1.mantissa
		
		SUB r11, #127						;2^(exponent1 + 127)
		SUB r12, #127						;2^(exponent2 + 127)
		ADD r11, r12						;2^(exponent1 + exponent2 + 2 * 127)
		ADD r11, #127					;result's exponent = 2^(exponent1 + exponent2 + 127)
		
		MOV r6, r6, LSL #1						; mantissa1 + LSB's before pattern
		MOV r8, #0							;result reg

MulLoop			;Mul Mantissa by 4-radix booth's algorithm	
		AND r12, r6, #7					;Check last 3-bits pattern
		
		CMP r12, #0						;if ADD 0 pattern
		BEQ MulShift						;Shift
		
		CMP r12, #2						;if ADD Multiplicand pattern
		ADDLE r8, r8, r7, LSL #2		;add at r8[25:2]
		BLE MulShift						;Shift
		
		CMP r12, #3						;if ADD 2 * Multiplicand pattern
		ADDEQ r8, r8, r7, LSL #3		;add at r8[26:3]
		BEQ MulShift						;Shift
		
		CMP r12, #4						;if SUB 2 * Multiplicand pattern
		SUBEQ r8, r8, r7, LSL #3		;sub at r8[26:3]
		BEQ MulShift						;Shift
		
		CMP r12, #6						;if SUB Multiplicand pattern
		SUBLE r8, r8, r7, LSL #2		;sub at r8[25:3]
												;if SUB Multipliand pattern or SUB 0 pattern, Shift
MulShift		;Shift r8 and check Calculate is ended
		
		MOV r6, r6, LSR #2				;shift Muliplier
		
		CMP r6, #0							;if Muliplier is not 0
		MOVNE r8, r8, ASR #2			;Result ASR 2 bits
		BNE MulLoop						;Repeat MulLoop
		
		
		CMP r8, #0x02000000			;if r8 is not 26 bit length(25 bit)
		BICLT r8, r8, #0x01000000		;delete first 1
		ANDLT r7, r8, #0x00000001	;Save lat LSB's pattern
		
		MOV r8, r8, LSR #1				;25 bit->24 bit or 26 bit -> 25 bit

		ANDGE r7, r8, #0x00000001		;if r8 is 25 bit length, Save last LSB's pattern
		MOVGE r8, r8, LSR #1			;if 25 bit -> 24 bit
	
		ADD r8, r7							;Rounding off
		MOV r11, r11, LSL #24			
		ADD r8, r8, r11, LSR #1			;sign(0) + Exponent + Mantissa
		MOV pc, lr							;End Function
		
		
		
;Adding positive two float number		
FloatingAdd						;result will store at r8
		MOV r11, r9, LSL #1		;delete sign
		MOV r12, r10, LSL #1
		
		CMP r11, #0				;check 0
		MOVEQ r8, r12
		MOVEQ pc, lr
		
		CMP r12, #0
		MOVEQ r8, r11
		MOVEQ pc, lr
		
		MOV r11, r11, LSR #24		;r11 and r12 is exponent
		MOV r12, r12, LSR #24
		
		MOV r6, r9, LSL #9			;r6 and r7 is mantissa LSL 1
		MOV r7, r10, LSL #9
		MOV r6, r6, LSR #8
		MOV r7, r7, LSR #8
		
		ADD r6, #0x01000000			;1.mantissa
		ADD r7, #0x01000000
		
		CMP r11, r12					;Compare two exponent
		;When r11 >= r12
		SUBGE r8, r11, r12			;Bigger - smaller
		MOVGE r7, r7, LSR r8		;smaller mantissa / (2 * (bigger - smaller))
		MOVGE r8, r11					;Save bigger's exponent
		
		;When r11 < r12
		SUBLT r8, r12, r11
		MOVLT r6, r6, LSR r8
		MOVLT r8, r12
		
		ADD r6, r7						;Adding Mantissa
		
		CMP r6, #0x02000000		;if Mantissa is 26 bits
		MOVGE r6, r6, LSR #1		;Make 25 bits
		ADDGE r8, #1					;Exponent++

		

		BIC r6, r6, #0x01000000	;25 bit -> 24 bit
		AND r7, r6, #0x00000001	;remember mantissa's LSB pattern
		MOV r6, r6, LSR #1			;24 bit -> 23 bit
		ADD r6, r7						;Rounding off

;Combine sign + exponent + Mantissa
		MOV r8, r8, LSL #24
		MOV r8, r8, LSR #1
		ADD r8, r6
		MOV pc, lr						;End Function

		LTORG
		
